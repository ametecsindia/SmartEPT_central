<p>We keep refunds simple and honest. SmartEPT offers a 7-day free trial precisely so you can
evaluate everything before paying — but if a first purchase still doesn't work out, this policy
has you covered.</p>

<h2>1. 7-day money-back on your first subscription purchase</h2>
<p>If you are not satisfied with your <b>first subscription purchase</b> (monthly or annual), write
to us within <b>7 days of payment</b> and we will refund the full amount. The licence is deactivated
on refund. The one-time Setup &amp; Onboarding fee is included in this money-back window if no
onboarding session has yet been delivered.</p>

<h2>2. What is not refundable</h2>
<ul>
  <li><b>Renewals</b> — renewal payments extend an existing, evaluated service and are not refundable.
  To stop, simply do not renew: there is no auto-charge without your action, and your licence runs to
  the end of the paid period.</li>
  <li><b>Used service periods</b> — months or days already consumed are not refunded.</li>
  <li><b>Cloud storage charges</b> — metered charges for storage already used.</li>
  <li><b>Perpetual licences and AMC</b> — once the licence key is issued, a perpetual licence sale is
  final; evaluate via the trial first. AMC covers a service year and is not refundable after start.</li>
</ul>

<h2>3. Pro-rata refunds</h2>
<p>Outside the cases above (for example, a genuine unresolved technical failure on our side, or an
accidental duplicate purchase), Ametecs may at its sole discretion issue a pro-rata refund for the
unused portion of a subscription. Duplicate payments for the same order are always refunded in full.</p>

<h2>4. How to request a refund</h2>
<p>Email <a href="mailto:support@ametecsindia.com">support@ametecsindia.com</a> (or WhatsApp
90000 98877) from your registered account email with your invoice number (format
EPT-YYYY-YY-MM-####) and the reason. We acknowledge within 2 business days.</p>

<h2>5. How refunds are paid</h2>
<p>Approved refunds go back to the original payment method — Razorpay/Stripe refunds typically
reach you in 5–10 business days depending on your bank; NEFT/UPI-recorded payments are refunded by
bank transfer to the paying account. Where GST has been remitted, a credit note is issued against
the original tax invoice.</p>

